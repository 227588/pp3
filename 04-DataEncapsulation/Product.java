public class Product {
    private String name;
    private boolean vege;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public boolean isVege() {
        return vege;
    }
    public void setVege(boolean vege) {
        this.vege = vege;
    }
}
